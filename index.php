        <h2> Descrizione dell'applicazione </h2>
        <p>
            L’applicazione supporta il noleggio di libri da una biblioteca. 
            La funzionalità di base prevede che un operatore possa inserire i dati 
            relativi al noleggio di un libro da parte di un utente. 
            I dati che si necessita per il noleggio sono:
        </p>
        <ul>
            <li>Nome, cognome dell'utente</li>
            <li>Nome e cognome dell'operatore che attiva il noleggio</li>
            <li>Nome e cognome dell'operatore che chiude il noleggio</li>
            <li>Giorno e ora di inizio e fine noleggio</li>
            <li>Titolo e autore del libro noleggiato</li>
        </ul>


        <p>L'utente &egrave in grado di visualizzare i noleggi attivi e conclusi
		   e una lista di tutti i libri noleggiabili con le seguenti caratteristiche:
        </p>
        <ul>
            <li>Un titolo</li>
            <li>Un autore</li>
            <li>Un anno di uscita</li>
            <li>Un genere</li>
			<li>Un ISBN</li>
			<li>Un genere</li>
		</ul>

        <p>L’applicazione mantiene una anagrafica degli utenti e degli operatori, in particolare:</p>

        <ul>
            <li>Nome e Cognome</li>
            <li>Indirizzo (Solo degli utenti)</li>
            <li>Email</li>
			<li>Un cellulare</li>
        </ul>

        <p>
            Inoltre l'operatore può aggiungere, eliminare, modificare e disabilitare utenti
			Cercare tra i noleggi per utente che ha effettuato il noleggio, libro, status (Aperto o concluso) e in scadenza.
			Aggiungere, modificare, elimiare una voce a catalogo o i dati di un libro.
		</p>
		
		<p>
            L'utente così come l'operatore possono modificare i propri dati
			e cambiare la password di login.
		</p>

